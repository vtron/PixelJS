Pixel.getBrowserName = function() {
	if((navigator.userAgent.match(/iPhone/i)) || (navigator.userAgent.match(/iPod/i))) {
		return Pixel.BROWSER_TYPE_IPHONE;
	} else {
		return Browser.name;
	}
}

